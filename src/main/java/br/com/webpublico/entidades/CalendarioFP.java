package br.com.webpublico.entidades;

import br.com.webpublico.geradores.GrupoDiagrama;
import br.com.webpublico.util.anotacoes.Etiqueta;
import br.com.webpublico.util.anotacoes.Obrigatorio;
import br.com.webpublico.util.anotacoes.Pesquisavel;
import br.com.webpublico.util.anotacoes.Tabelavel;
import org.hibernate.envers.Audited;

import javax.persistence.*;
import java.io.Serializable;
import java.util.ArrayList;
import java.util.List;

/**
 * @author andre
 */
@Entity

@Audited
@GrupoDiagrama(nome = "RecursosHumanos")
@Etiqueta("Calend√°rio FP")
public class CalendarioFP implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.AUTO)
    private Long id;
    @Tabelavel
    @Pesquisavel
    @Obrigatorio
    @Etiqueta("Ano")
    private Integer ano;
    @OneToMany(mappedBy = "calendarioFP", cascade = CascadeType.ALL, orphanRemoval = true)
    private List<ItemCalendarioFP> itemCalendarioFPs;

    public CalendarioFP() {
        itemCalendarioFPs = new ArrayList<ItemCalendarioFP>();
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Integer getAno() {
        return ano;
    }

    public void setAno(Integer ano) {
        this.ano = ano;
    }

    public List<ItemCalendarioFP> getItemCalendarioFPs() {
        return itemCalendarioFPs;
    }

    public void setItemCalendarioFPs(List<ItemCalendarioFP> itemCalendarioFPs) {
        this.itemCalendarioFPs = itemCalendarioFPs;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof CalendarioFP)) {
            return false;
        }
        CalendarioFP other = (CalendarioFP) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return ano.toString();
    }
}
