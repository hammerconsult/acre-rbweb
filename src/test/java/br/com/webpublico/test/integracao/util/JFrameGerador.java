    private void jButtonGerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGerarActionPerformed

        try {
            IDatabaseConnection conn = getConnection();
            String[] depTableNames = null;
            if (jCheckBoxDependencias.isSelected()) {
                depTableNames = TablesDependencyHelper.getAllDependentTables(conn, jTextFieldTabela.getText());

            } else {
                depTableNames = new String[]{jTextFieldTabela.getText()};
            }
            IDataSet depDataset = conn.createDataSet(depTableNames);
            int returnVal = fileChooser.showOpenDialog(JFrameGerador.this);

            if (returnVal == JFileChooser.APPROVE_OPTION) {
                File f = fileChooser.getSelectedFile();
                FlatXmlDataSet.write(depDataset, new FileOutputStream(f));
                JOptionPane.showMessageDialog(this, "Arquivo gerado...");
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Erro ao gerar arquivo" + ex.getMessage());
        }

    }//GEN-LAST:event_jButtonGerarActionPerformed

